void  Clk_DCOInit (void)
{
    /* Set DCO to 16 MHz */
    if (CALBC1_16MHZ != 0xFF && CALDCO_16MHZ != 0xFF)                                     
    {  
        BCSCTL1 = CALBC1_16MHZ;
        DCOCTL = CALDCO_16MHZ;
    }
}

void Clk_ACLK_div(INT08U div)
{
  switch(div)
  {
    case ACLK_DIV_1:
    	BIT_CLR(BCSCTL1,DIVA0);
    	BIT_CLR(BCSCTL1,DIVA1);
      break;
    case ACLK_DIV_2:
    	BIT_SET(BCSCTL1,DIVA0);
    	BIT_CLR(BCSCTL1,DIVA1);
      break;
    case ACLK_DIV_4:
    	BIT_CLR(BCSCTL1,DIVA0);
    	BIT_SET(BCSCTL1,DIVA1);
      break;
    case ACLK_DIV_8:
    	BIT_SET(BCSCTL1,DIVA0);
    	BIT_SET(BCSCTL1,DIVA1);
      break;
    default:
    	BIT_CLR(BCSCTL1,DIVA0);
    	BIT_CLR(BCSCTL1,DIVA1);
      break;
  }
   
}

